public class ReportsAide {
    private String StaffNumberR;
    private String StaffNameR;
    private String StaffContactR;
    private String StaffEmailR;
    private String StaffCityR;

    public ReportsAide(String staffNumberR, String staffNameR, String staffContactR, String staffEmailR, String staffCityR) {
        StaffNumberR = staffNumberR;
        StaffNameR = staffNameR;
        StaffContactR = staffContactR;
        StaffEmailR = staffEmailR;
        StaffCityR = staffCityR;
    }

    public String getStaffNumberR() {
        return StaffNumberR;
    }

    public void setStaffNumberR(String staffNumberR) {
        StaffNumberR = staffNumberR;
    }

    public String getStaffNameR() {
        return StaffNameR;
    }

    public void setStaffNameR(String staffNameR) {
        StaffNameR = staffNameR;
    }

    public String getStaffContactR() {
        return StaffContactR;
    }

    public void setStaffContactR(String staffContactR) {
        StaffContactR = staffContactR;
    }

    public String getStaffEmailR() {
        return StaffEmailR;
    }

    public void setStaffEmailR(String staffEmailR) {
        StaffEmailR = staffEmailR;
    }

    public String getStaffCityR() {
        return StaffCityR;
    }

    public void setStaffCityR(String staffCityR) {
        StaffCityR = staffCityR;
    }
}
